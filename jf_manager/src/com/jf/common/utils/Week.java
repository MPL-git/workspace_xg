package com.jf.common.utils;

public class Week {  
	  
    String weekBegin;  
    String weekEnd;  
  
    public String getWeekBegin() {  
        return weekBegin;  
    }  
  
    public void setWeekBegin(String weekBegin) {  
        this.weekBegin = weekBegin;  
    }  
  
    public String getWeekEnd() {  
        return weekEnd;  
    }  
  
    public void setWeekEnd(String weekEnd) {  
        this.weekEnd = weekEnd;  
    }  
}

package com.jf.entity;


public class IntegralTypeCustom extends IntegralType{
	private String tallyModeName;
	private String statusDesc;
	private String staffName;
	private String intTypeDesc;
	private String growTypeDesc;

	public String getTallyModeName() {
		return tallyModeName;
	}
	public void setTallyModeName(String tallyModeName) {
		this.tallyModeName = tallyModeName;
	}
	public String getStatusDesc() {
		return statusDesc;
	}
	public void setStatusDesc(String statusDesc) {
		this.statusDesc = statusDesc;
	}
	public String getStaffName() {
		return staffName;
	}
	public void setStaffName(String staffName) {
		this.staffName = staffName;
	}
	public String getIntTypeDesc() {
		return intTypeDesc;
	}
	public void setIntTypeDesc(String intTypeDesc) {
		this.intTypeDesc = intTypeDesc;
	}
	public String getGrowTypeDesc() {
		return growTypeDesc;
	}
	public void setGrowTypeDesc(String growTypeDesc) {
		this.growTypeDesc = growTypeDesc;
	}
}

package com.jf.entity;


public class MchtInfoEntity {
	private MchtInfo mchtInfo;
	
	private String regCapital;

	private String yearlyInspectionDate;
	
	private Integer pic;
	
	public Integer getPic() {
		return pic;
	}

	public void setPic(Integer pic) {
		this.pic = pic;
	}

	public MchtInfo getMchtInfo() {
		return mchtInfo;
	}

	public void setMchtInfo(MchtInfo mchtInfo) {
		this.mchtInfo = mchtInfo;
	}

	public String getRegCapital() {
		return regCapital;
	}

	public void setRegCapital(String regCapital) {
		this.regCapital = regCapital;
	}

	public String getYearlyInspectionDate() {
		return yearlyInspectionDate;
	}

	public void setYearlyInspectionDate(String yearlyInspectionDate) {
		this.yearlyInspectionDate = yearlyInspectionDate;
	}
	
	
	
}

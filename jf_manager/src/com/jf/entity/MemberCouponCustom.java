package com.jf.entity;


public class MemberCouponCustom extends MemberCoupon{
	private String statusDesc;
	private String couponName;
	private String nick;
	private String money;
	private String minimum;
	private String mobile;
	private String rangName;
	private String combineOrderCode;

	public String getStatusDesc() {
		return statusDesc;
	}
	public void setStatusDesc(String statusDesc) {
		this.statusDesc = statusDesc;
	}
	public String getCouponName() {
		return couponName;
	}
	public void setCouponName(String couponName) {
		this.couponName = couponName;
	}
	public String getNick() {
		return nick;
	}
	public void setNick(String nick) {
		this.nick = nick;
	}
	public String getMoney() {
		return money;
	}
	public void setMoney(String money) {
		this.money = money;
	}
	public String getMinimum() {
		return minimum;
	}
	public void setMinimum(String minimum) {
		this.minimum = minimum;
	}
	public String getMobile() {
		return mobile;
	}
	public void setMobile(String mobile) {
		this.mobile = mobile;
	}
	public String getRangName() {
		return rangName;
	}
	public void setRangName(String rangName) {
		this.rangName = rangName;
	}
	public String getCombineOrderCode() {
		return combineOrderCode;
	}
	public void setCombineOrderCode(String combineOrderCode) {
		this.combineOrderCode = combineOrderCode;
	}
	
	
}

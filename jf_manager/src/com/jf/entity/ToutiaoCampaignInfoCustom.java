package com.jf.entity;

public class ToutiaoCampaignInfoCustom extends ToutiaoCampaignInfo {

	private String advertiserName;
	private String landingTypeDesc;
	private String budgetModeDesc;
	private String statusDesc;

	public String getAdvertiserName() {
		return advertiserName;
	}

	public void setAdvertiserName(String advertiserName) {
		this.advertiserName = advertiserName;
	}

	public String getLandingTypeDesc() {
		return landingTypeDesc;
	}

	public void setLandingTypeDesc(String landingTypeDesc) {
		this.landingTypeDesc = landingTypeDesc;
	}

	public String getBudgetModeDesc() {
		return budgetModeDesc;
	}

	public void setBudgetModeDesc(String budgetModeDesc) {
		this.budgetModeDesc = budgetModeDesc;
	}

	public String getStatusDesc() {
		return statusDesc;
	}

	public void setStatusDesc(String statusDesc) {
		this.statusDesc = statusDesc;
	}

}
